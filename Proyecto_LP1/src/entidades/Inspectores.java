package entidades;

public class Inspectores {
	
	private int codigo, telefonoIns;
	private String nombreIns, apellidoIns;
	private String fecha;
	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getTelefonoIns() {
		return telefonoIns;
	}
	public void setTelefonoIns(int telefonoIns) {
		this.telefonoIns = telefonoIns;
	}
	public String getNombreIns() {
		return nombreIns;
	}
	public void setNombreIns(String nombreIns) {
		this.nombreIns = nombreIns;
	}
	public String getApellidoIns() {
		return apellidoIns;
	}
	public void setApellidoIns(String apellidoIns) {
		this.apellidoIns = apellidoIns;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	

}
